                        The Box League is to encourage competative and friendly play between squash and racketball players in  Sutton.

What is the purpose and spirit of the league?
The league functions for friendly, social competition. Players are expected to be polite and
respectful, and players with a poor attitude are liable to be removed from the league.
Let calls should only be a point (“stroke”) if you are both certain. If in doubt about any call,
replay the point.

How does the league work?
Each period you must contact the other players in your box and play a match against them.
Agree the scoring format in advance – it’s up to you. All that counts is who wins so you can
play a set, three sets, 45 minutes or simply toss a coin! All matches should be played
according to WSF rules.

Who should contact whom?
You will be entered into a WhatsApp group with other members of your box.
Please contact everyone in your box. Do not wait for others! There are no points awarded
for un-played games unless one person concedes the match due to being unavailable.

How can I be removed from the squash league?
If you no longer wish to be listed on the league any longer then please email your request to be removed to contact@suttonsquash.club
FAQ’s
How do I contact players?
Upon registering on the squash League you will be signed up to squashlevels box leagues.
You will be entered into a WhatsApp group with other members of your box. Please message in the groups or message other members privately.

How are the starting positions on the league determined?
Starting positions in the league are determined at the start by levels collected in the sign up form and each cycle thereafter will follow on from those results.

Rules of play
League matches will be best of five matches (i.e. first to 3 wins).
League matches are scored to 11 points, played in accordance with the rules of the World Squash Federation, unless both
players agree otherwise.
A single yellow-dot ball should be used, unless both players agree otherwise.
All players are responsible for organising their own matches.

